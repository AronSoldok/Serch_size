def t(lang: str, key: str, **kwargs: str | int) -> str:
    language = lang if lang in LANGUAGES else DEFAULT_LANG
    template = _STRINGS[language].get(key) or _STRINGS[DEFAULT_LANG].get(key, key)
    if kwargs:
        return template.format(**kwargs)
    return template


def default_language() -> str:
    try:
        code = locale.getdefaultlocale()[0] or ""
    except (ValueError, AttributeError):
        code = ""
    code = code.lower().replace("_", "-")
    mapping = {
        "ru": "ru",
        "zh": "zh",
        "zh-cn": "zh",
        "es": "es",
        "de": "de",
        "fr": "fr",
        "ja": "ja",
        "pt": "pt",
        "pt-br": "pt",
    }
    for prefix, lang in mapping.items():
        if code.startswith(prefix):
            return lang
    return DEFAULT_LANG


def unit_labels(lang: str) -> list[str]:
    return [t(lang, "unit_b"), t(lang, "unit_kb"), t(lang, "unit_mb"), t(lang, "unit_gb")]


def unit_multipliers(lang: str) -> dict[str, int]:
    return {
        t(lang, "unit_b"): 1,
        t(lang, "unit_kb"): 1024,
        t(lang, "unit_mb"): 1024**2,
        t(lang, "unit_gb"): 1024**3,
    }


def default_unit(lang: str) -> str:
    return t(lang, "unit_mb")
